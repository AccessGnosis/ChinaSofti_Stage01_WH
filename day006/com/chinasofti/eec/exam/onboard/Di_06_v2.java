package com.chinasofti.eec.exam.onboard;

public class Di_06_v2 {

	public static void main(String[] args) {
		int num = 1020;
		int day = 0;
		do {
			num = num - (num / 2 + 2);
			day++;
			System.out.println("第" + day + "天后剩余" + num + "个西瓜");
		} while (num > 0);
		System.out.println("第" + day + "天后卖完");
	}

}
