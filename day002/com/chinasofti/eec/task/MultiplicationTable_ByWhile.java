package com.chinasofti.eec.task;

public class MultiplicationTable_ByWhile {

	public static void main(String[] args) {
		int length = 10;
		int i = 1, j = 1;
		while (i <= length) {
			while (j <= length) {
				System.out.print(i + "¡Á" + j + "=" + (i * j) + "  ");
				if (i == j) {
					j = 1;
					break;
				}
				++j;
			}
			System.out.println("");
			++i;
		}
	}

}
