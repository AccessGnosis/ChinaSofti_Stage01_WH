package com.chinasofti.eec.day002;

public class SwitchProgramDemo {

	public static void main(String[] args) {
		// switch(整形表达式){
		// case 整形常量值1:
		// 语句1;
		// 语句2;
		// (break; 结束switch)
		// case 整形常量值2:
		// 语句3;
		// 语句4;
		// ……
		// default:
		// 语句5;
		// 语句6;
		// (break; 作用：跳出switch结构)
		// }
		int num = 2;
		switch (num) { // jdk 1.7版本以下只能包含整形常量，jdk 1.7版本以上num可以是字符串
		case 1:
			System.out.println("111");
			break;
		case 2:
			System.out.println("222");
			break;
		case 3:
			System.out.println("333");
			break;
		default:
			System.out.println("未知");
		}
	}
}
